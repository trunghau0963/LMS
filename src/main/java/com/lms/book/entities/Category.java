package com.lms.book.entities;

public class Category {
    private String id;
    private String genre;

    public String getId() {
        return id;
    }

    public String getGenre() {
        return genre;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }
}
